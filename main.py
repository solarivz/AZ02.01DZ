import pandas as pd


# Создаем DataFrame с данными оценок учеников по 5 предметам
data = {
    'Ученик': ['Ученик 1', 'Ученик 2', 'Ученик 3', 'Ученик 4', 'Ученик 5',
               'Ученик 6', 'Ученик 7', 'Ученик 8', 'Ученик 9', 'Ученик 10'],
    'Математика': [85, 78, 92, 70, 88, 94, 65, 82, 77, 90],
    'Физика': [80, 75, 85, 60, 78, 89, 73, 84, 72, 88],
    'Химия': [79, 82, 91, 68, 77, 85, 62, 87, 81, 90],
    'История': [88, 72, 86, 79, 83, 80, 70, 75, 82, 90],
    'Литература': [91, 89, 84, 78, 87, 88, 73, 90, 85, 92]
}

df = pd.DataFrame(data)

# Выводим первые несколько строк DataFrame
first_rows = df.head()

# Вычисляем среднюю оценку по каждому предмету
mean_scores = df[['Математика', 'Физика', 'Химия', 'История', 'Литература']].mean()

# Вычисляем медианную оценку по каждому предмету
median_scores = df[['Математика', 'Физика', 'Химия', 'История', 'Литература']].median()

# Вычисляем Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)

# Вычисляем IQR (межквартильный размах) для математики
IQR_math = Q3_math - Q1_math

# Вычисляем стандартное отклонение по каждому предмету
std_deviation = df[['Математика', 'Физика', 'Химия', 'История', 'Литература']].std()

# Выводим все вычисления на консоль
print("Первые несколько строк DataFrame:\n", first_rows, "\n")
print("Средние оценки по каждому предмету:\n", mean_scores, "\n")
print("Медианные оценки по каждому предмету:\n", median_scores, "\n")
print(f"Первый квартиль (Q1) по математике: {Q1_math}")
print(f"Третий квартиль (Q3) по математике: {Q3_math}")
print(f"Межквартильный размах (IQR) по математике: {IQR_math}\n")
print("Стандартное отклонение по каждому предмету:\n", std_deviation)